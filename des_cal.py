# Load your uploaded CSV
df = pd.read_csv("cleaned_toxicity.csv")

print("shape of the dataset ",df.shape)
print(df.head())
# calcuate 2D descriptors
calc = Calculator(descriptors, ignore_3D=True)
mols = [Chem.MolFromSmiles(smile) for smile in df["SMILES"]]

#calculating descriptors
descriptor_df = calc.pandas(mols)
print(descriptor_df.head())
print(descriptor_df.shape)

descriptor_df = descriptor_df.apply(pd.to_numeric, errors='coerce')
descriptor_df = descriptor_df.dropna(axis=1)
descriptor_df = descriptor_df.loc[:, descriptor_df.std() > 0]
print("shape of the dataframe before a cross correlation ", descriptor_df.shape)

#Mutual Information
mi_scores = mutual_info_regression(descriptor_df, df["LD50"] )
mi_results = pd.Series(mi_scores, index=descriptor_df.columns)

#Calculate Cross-Correlation between descriptors
corr_matrix = descriptor_df.corr().abs()
upper = corr_matrix.where(np.triu(np.ones(corr_matrix.shape), k=1).astype(bool))

drop_des = set()

for col in upper.columns:
    correlated_rows = upper.index[upper[col] > 0.95].tolist()

    for row  in correlated_rows:
      if mi_results[row] >= mi_results[col]:
        drop_des.add(col)
      else:
        drop_des.add(row)

new_df = descriptor_df.drop(columns=list(drop_des))
print("shape after the cross correlation removal ",new_df.shape)

final_df = pd.concat([df["SMILES"], df["LD50"], new_df], axis=1)
final_df = final_df.dropna().reset_index(drop=True)
print("final dataset shape:", final_df.shape)
print(final_df.head())

# save the final dataset
final_df.to_csv("filtered_descriptors.csv", index=False)
